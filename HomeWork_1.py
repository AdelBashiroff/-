# Ввод натурального числа n
n = int(input("Введите натуральное число n (n <= 100): "))

# a) Сколько цифр в числе n?
num_digits = len(str(n))

# b) Сумма цифр числа n
sum_digits = sum(int(digit) for digit in str(n))

# c) Первая цифра числа n
first_digit = int(str(n)[0])

# d) Предпоследняя цифра числа n (предположим, что n >= 10)
if n >= 10:
    second_last_digit = int(str(n)[-2])
else:
    second_last_digit = None  # Если число меньше 10, предпоследней цифры нет

# Вывод результатов
print(f"a) Количество цифр в числе: {num_digits}")
print(f"b) Сумма цифр числа: {sum_digits}")
print(f"c) Первая цифра числа: {first_digit}")
if second_last_digit is not None:
    print(f"d) Предпоследняя цифра числа: {second_last_digit}")
else:
    print("d) Предпоследней цифры нет (число меньше 10).")

