# Константа PI
PI = 3.141592653589793

# Ввод чисел
m = int(input("Введите целое число m: "))
n = int(input("Введите целое число n (не равное m): "))

# Проверка на равенство
if m == n:
    print("Числа не должны быть равны.")
else:
    # Вычисление дробей
    a = m / n
    b = n / m

    # Расчёт расстояний до PI
    d1 = abs(PI - a)
    d2 = abs(PI - b)

    # Определяем, какая дробь ближе
    if d1 < d2:
        m *= 10
        print("Дробь m/n ближе к PI. Новое значение m:", m)
    else:
        n *= 2
        print("Дробь n/m ближе к PI. Новое значение n:", n)

