# Ввод количества чисел
n = int(input("Введите количество чисел: "))

# Счётчик чисел, удовлетворяющих условию
count = 0

# Функция для проверки, что все цифры числа либо четные, либо нечетные
def check_digits(num):
    digits = [int(digit) for digit in str(num)]
    even_digits = all(d % 2 == 0 for d in digits)
    odd_digits = all(d % 2 != 0 for d in digits)
    return even_digits or odd_digits

# Ввод и проверка чисел
for _ in range(n):
    num = int(input("Введите число: "))
    if len(str(num)) == 3 or len(str(num)) == 5:  # проверяем длину числа
        if check_digits(num):  # проверяем, что все цифры либо четные, либо нечетные
            count += 1

# Проверка, что существует ровно два таких числа
if count == 2:
    print("Да, существует ровно два таких числа.")
else:
    print("Нет, таких чисел:", count)

