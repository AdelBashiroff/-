# Ввод размера массива
n = int(input("Введите размер массива n: "))

# Создаём массив
a = []

# Заполняем массив по заданному принципу
for i in range(n):
    # Если индекс чётный, то элемент положительный, если нечётный — отрицательный
    if i % 2 == 0:
        a.append(1 + 2 * (i // 2))
    else:
        a.append(-1 - 2 * (i // 2))

# Выводим полученный массив
print("Полученный массив:")
print(a)

